function renderSpeakers(items) {
  const container = document.getElementById("speaker-cards");
  container.innerHTML = items
    .map(
      (item) => {
        const bodyMarkup = Array.isArray(item.paragraphs)
          ? item.paragraphs
              .map((paragraph, index) => {
                const paragraphClass = index === 0 ? "speaker-card-message-title" : "speaker-card-message-body";
                return `<p class="${paragraphClass}">${paragraph}</p>`;
              })
              .join("")
          : `<p class="speaker-card-message-body">${item.text}</p>`;

        const signatureMarkup = Array.isArray(item.signature)
          ? `
            <div class="speaker-card-signature">
              ${item.signature.map((line) => `<span>${line}</span>`).join("")}
            </div>
          `
          : "";

        return `
        <article class="speaker-card" data-reveal>
          <div class="speaker-card-media">
            <img src="${item.image}" alt="${item.name}" />
          </div>
          <div class="speaker-card-copy">
            <p class="speaker-card-role">${item.role}</p>
            <h3>${item.name}</h3>
            ${bodyMarkup}
            ${signatureMarkup}
          </div>
        </article>
      `;
      }
    )
    .join("");
}

