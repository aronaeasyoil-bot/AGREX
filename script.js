function getByPath(object, path) {
  return path.split(".").reduce((value, key) => (value ? value[key] : undefined), object);
}

function setMeta(data, lang) {
  document.documentElement.lang = lang;
  document.title = data.meta.title;

  const description = document.querySelector('meta[name="description"]');
  const ogDescription = document.querySelector('meta[property="og:description"]');
  const ogLocale = document.querySelector('meta[property="og:locale"]');

  if (description) description.setAttribute("content", data.meta.description);
  if (ogDescription) ogDescription.setAttribute("content", data.meta.description);
  if (ogLocale) ogLocale.setAttribute("content", lang === "fr" ? "fr_FR" : "en_GB");
}

function applyTexts(data) {
  document.querySelectorAll("[data-text]").forEach((element) => {
    const value = getByPath(data, element.dataset.text);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  Object.entries(data.nav).forEach(([key, value]) => {
    document.querySelectorAll(`[data-nav="${key}"]`).forEach((element) => {
      element.textContent = value;
    });
  });
}

